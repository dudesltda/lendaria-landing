function titleFromMarkdown(markdown, file) {
  const heading = markdown.match(/^#\s+(.+)$/m);
  if (heading) return heading[1].trim();
  return file.replace(/ [a-f0-9]{32}\.md$/i, "").replace(/\.md$/i, "");
}

const readings = orderedFiles.map((file, index) => {
  const absolutePath = path.join(sourceRoot, ...file.split("/"));
  const markdown = fs.readFileSync(absolutePath, "utf8").trimEnd();
  const lineCount = markdown.split(/\r?\n/).length;
  return {
    index: index + 1,
    file,
    title: titleFromMarkdown(markdown, file),
    lineCount,
    markdown,
  };
});

